public class VetorInteiros {
    public static void main(String[] args) {
        int[] numeros = new int[5]; // declara o vetor com 5 posições

        // atribuindo valores
        numeros[0] = 10;
        numeros[1] = 20;
        numeros[2] = 30; // terceira posição (índice 2)
        numeros[3] = 40;
        numeros[4] = 50;

        // imprime o valor na terceira posição
        System.out.println("O valor na terceira posição é: " + numeros[2]);
    }
}
